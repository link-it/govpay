			List<IField> fields = new ArrayList<>();

			fields.add(Batch.model().COD_BATCH);

			List<Map<String, Object>> returnMap = this.select(jdbcProperties, log, connection, sqlQueryObject, expression, fields.toArray(new IField[1]));

